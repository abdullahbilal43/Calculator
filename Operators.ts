let Add=(num1:number,num2:number)=>{
return num1+num2;
}

let Multiply=(num1:number,num2:number)=>{
    return num1*num2;
}

let Subtract=(num1:number,num2:number)=>{
    return num1-num2;
}

let Divide=(num1:number,num2:number)=>{
    return num1/num2;
}
export {Add,Multiply,Subtract,Divide}